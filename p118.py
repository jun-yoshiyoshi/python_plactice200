ran = range(5)
print(f"range(5):{ran}")

for _ in ran:
    print(_)

ran = range(5, 10)
print(f"range(5,10):{ran}")
for _ in ran:
    print(_)

ran = range(5, 15, 3)
print(f"range(5,15,3):{ran}")
for _ in ran:
    print(_)

ran = range(20, 1, -3)
print(f"range(20,1,-3):{ran}")
for _ in ran:
    print(_)
